    # Methods of list         " list --> [] "
 
name = ['Kavitha','Chinni','Babi','Nitish']

name.append('Shiva')  # append() Adds an element at end of the list
print(name)

name.pop(1)    # pop(index) Delete an element at that index
print(name)

name.pop()    #pop() Delete an element at last index
print(name)

name.append('Babi')
print(name)

name.remove('Babi')    #remove Deletes the first occurance of repeated item
print(name)

name.insert(3,'shiva')    # inserts item at the given index
print(name)

name.clear()        # clears entire list
print(name)

