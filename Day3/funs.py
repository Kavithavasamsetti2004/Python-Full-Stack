  
      # Definig all the fuctions to perform calculations

def add(x,y):
    result = x+y
    return result
def sub(p,q):
    result = p-q
    return result
def mul(k,l):
    result = k*l
    return result
def div(a,b):
    result =a/b
    return result
def pow(m,n):
    result = m**n
    return result
def mod(r,s):
    result = r % s
    return result

